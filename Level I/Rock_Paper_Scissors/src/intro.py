def intro_message(name: str):
    """Prints the intro message for the game.
    Args:
        name: The name of the player.
    """
    print(f"Welcome to our Rock Paper Scissors game {name}!")
    print("To play the game enter your choice in the prompt to see who wins!")
    print("If you wish to quit the game just enter 'q' or 'Q' in the prompt.")
    print("Let's dive into it!")
    print("-" * 60)
